ENE_D(I,J,K)=0.5d0*(ENE_ANT2_D(I,J,K)+ENE_D(I,J,K))
END IF
END IF

END SUBROUTINE RKCONSOLIDATE

ATTRIBUTES(GLOBAL) SUBROUTINE SLOPELIMITER(U_D,SLOPES_D,BOUNDS_D,NX,NY,NZ,DIST_MAX,ALFA,TETA,INVERSOR_X,INVERSOR_Z)
USE ISO_C_BINDING
IMPLICIT NONE
INTEGER(KIND=C_INT),VALUE :: NX,NY,NZ
REAL(KIND=C_DOUBLE),VALUE :: ALFA,TETA,INVERSOR_X,INVERSOR_Z,DIST_MAX
REAL(KIND=C_DOUBLE) 	  :: U_D(:,:,:)
REAL(KIND=C_DOUBLE) 	  :: SLOPES_D(:,:,:,:)
INTEGER(KIND=C_INT) 	  :: BOUNDS_D(:,:,:)
INTEGER(KIND=C_INT) 	  :: I,J,K
REAL(KIND=C_DOUBLE)				 :: U,PHI1,PHI2,PHI3,MM
REAL(KIND=C_DOUBLE),DIMENSION(6) :: V
!USE INVERSOR=1.0d0 IF DENSITY OR PRESSURE, INVERSOR=-1.0d0 IF MOMENTUM 

!RECOVERS INDEXES
I= BLOCKDIM%X * (BLOCKIDX%X - 1) + THREADIDX%X
J= BLOCKDIM%Y * (BLOCKIDX%Y - 1) + THREADIDX%Y
K= BLOCKDIM%Z * (BLOCKIDX%Z - 1) + THREADIDX%Z

IF ((I>1) .AND. (I<NX) .AND. (J>1) .AND. (J<NY) .AND. (K>1) .AND. (K<NZ)) THEN
IF(BOUNDS_D(I,J,K)==0) THEN
U=U_D(I,J,K)
!NEIGHBORS
V(1)=U_D(I-1,J,K)
V(2)=U_D(I+1,J,K)
V(3)=U_D(I,J-1,K)
V(4)=U_D(I,J+1,K)
V(5)=U_D(I,J,K-1)
V(6)=U_D(I,J,K+1)

IF(BOUNDS_D(I-1,J,K)==1) THEN !REFLECTIVE WALL LEFT
V(1)=INVERSOR_X*U_D(I,J,K)
END IF

IF(BOUNDS_D(I+1,J,K)==1) THEN !REFLECTIVE WALL RIGHT
V(2)=INVERSOR_X*U_D(I,J,K)
END IF

IF(BOUNDS_D(I,J,K-1)==1) THEN !REFLECTIVE FLOOR
V(5)=INVERSOR_Z*U_D(I,J,K)
END IF

IF(BOUNDS_D(I,J,K+1)==1) THEN !REFLECTIVE CEIL
V(6)=INVERSOR_Z*U_D(I,J,K)
END IF

!MM2
!X
PHI1=ALFA*TETA*(U-V(1))/DIST_MAX
PHI2=ALFA*(V(2)-V(1))/(2.0d0*DIST_MAX)
PHI3=ALFA*TETA*(V(2)-U)/DIST_MAX


MM=0.5d0*(SIGN(1.d0,PHI1)+SIGN(1.d0,PHI2))*MIN(ABS(PHI1),ABS(PHI2))
SLOPES_D(I,J,K,1)=0.5d0*(SIGN(1.d0,MM)+SIGN(1.d0,PHI3))*MIN(ABS(MM),ABS(PHI3))

!Y
PHI1=ALFA*TETA*(U-V(3))/DIST_MAX
PHI2=ALFA*(V(4)-V(3))/(2.0d0*DIST_MAX)
PHI3=ALFA*TETA*(V(4)-U)/DIST_MAX


MM=0.5d0*(SIGN(1.d0,PHI1)+SIGN(1.d0,PHI2))*MIN(ABS(PHI1),ABS(PHI2))
SLOPES_D(I,J,K,2)=0.5d0*(SIGN(1.d0,MM)+SIGN(1.d0,PHI3))*MIN(ABS(MM),ABS(PHI3))

!Z
PHI1=ALFA*TETA*(U-V(5))/DIST_MAX
PHI2=ALFA*(V(6)-V(5))/(2.0d0*DIST_MAX)
PHI3=ALFA*TETA*(V(6)-U)/DIST_MAX


MM=0.5d0*(SIGN(1.d0,PHI1)+SIGN(1.d0,PHI2))*MIN(ABS(PHI1),ABS(PHI2))
SLOPES_D(I,J,K,3)=0.5d0*(SIGN(1.d0,MM)+SIGN(1.d0,PHI3))*MIN(ABS(MM),ABS(PHI3))


END IF
END IF

END SUBROUTINE SLOPELIMITER

